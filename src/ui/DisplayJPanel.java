    private void displayProfile() {
        
        int i, j;
        
        ////////// Name ///////////////
        txtName.setText(profile.getName());
        ////////// Bitrh Date ///////////////
        txtBirthDate.setText(profile.getBirthDate());
        ////////// Telephone Number ///////////////
        if(profile.getTeleNum() != 0){
            String tempTN = Long.toString(profile.getTeleNum());
            txtTeleNum.setText('(' + tempTN.substring(0, 3) + ')' + tempTN.substring(3, 6) + '-' + tempTN.substring(6));  // display as (xxx)xxx-xxxx 
        }
        ////////// SSN ///////////////
        if(profile.getSsn() != 0){
            if(Integer.toString(profile.getSsn()).charAt(0) == 0 ){
                txtSSN.setText('0' + Integer.toString(profile.getSsn()));
            } else txtSSN.setText(Integer.toString(profile.getSsn()));
            switch(Integer.toString(profile.getSsn()).length()){
                case 4: txtSSN.setText(Integer.toString(profile.getSsn())); break;
                case 3: txtSSN.setText('0' + Integer.toString(profile.getSsn())); break;
                case 2: txtSSN.setText("00" + Integer.toString(profile.getSsn())); break;
                case 1: txtSSN.setText("000" + Integer.toString(profile.getSsn())); break;   // add missing 0 in the front
                default:
            }
        }
        ////////// Address ///////////////
        txtStreetName.setText(profile.getGeoStreet());
        txtZipcode.setText(profile.getGeoData());
        ///////////////   Bank Account Number   /////////////////////// 
        if(profile.getBankAccoNum()!= 0){
            String tempBA = Long.toString(profile.getBankAccoNum());
            txtBankAccount.setText(tempBA.substring(0, 4) +' '+ tempBA.substring(4, 8) + ' ' + tempBA.substring(8, 12) + ' '+tempBA.substring(12));  // display as xxxx xxxx xxxx xxxx
        }
        ///////////////   Email Address   /////////////////////// 
        txtEmail.setText(profile.getEmailAddr());
        ///////////////   Fax Number   ///////////////////////
        if(profile.getFaxNum()!= 0){
            txtFaxNum.setText(Integer.toString(profile.getFaxNum()));
        }
        ///////////////   MRN   ///////////////////////
       String mrn = Integer.toString(profile.getMrn());
        if(profile.getMrn()!= 0){
            if(mrn.length()<10){
                j = 7-mrn.length();
                for(i=0;i<j;i++){
                    mrn = '0' + mrn;
                }
            }
            txtMRN.setText(mrn);
        }
        ///////////////   Vehicle Id   ///////////////////////
        txtVehicleID.setText(profile.getVehicleId());
        ///////////////   Linkedin   ///////////////////////
        txtLinkedin.setText(profile.getLinkedin());
        ///////////////   IP   ///////////////////////
        txtIP.setText(profile.getInternetProtAddr());
        ///////////////   HBPN & Certificate Number   ///////////////////////
        txtHPBN.setText(profile.getHpbn());
        txtCertiNum.setText(profile.getCertiNum());
       

    }
