    private void btnViewFimgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewFimgActionPerformed
        // TODO add your handling code here:
        
        BufferedImage faceImg = null;
        try {
//            faceImg = ImageIO.read(new File(profile.getfImageAddr())); // load image through address(not convenient)
            faceImg = ImageIO.read(profile.getfImage());  // load image directly 
        } catch (IOException ex) {
            Logger.getLogger(DisplayJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        Image resizedFaceImg = faceImg.getScaledInstance(lblforimg.getWidth(), lblforimg.getHeight(), Image.SCALE_SMOOTH); // reshape the image to fit in the label
        ImageIcon imageIcon = new ImageIcon(resizedFaceImg); 
        lblforimg.setIcon(imageIcon);                     // set label's icon
        
    }//GEN-LAST:event_btnViewFimgActionPerformed

